    public static boolean[][] convertToBoolean(int [][] nodes){
        boolean[][] booleens = new boolean[nodes.length][nodes[0].length];
        for (int i = 0; i < nodes.length; i++) {
            for (int j = 0; j < nodes[0].length; j++) {
                if(nodes[i][j] == 1){
                    booleens[i][j] = true;
                }
                else {
                    booleens[i][j] = false;
                }
            }
        }
        return booleens;
    }
